NASTY_AB_VALUES = (
    (0, 0),
    (1, 0),
    (0, 1),
    (1, 1),
    (255, 0),
    (0, 255),
    (255, 255),
    (256, 0),
    (0, 256),
    (256, 256),
    (260, 513),
    (65535, 0),
    (0, 65535),
    (65535, 65535),
    (65536, 0),
    (0, 65536),
    (65536, 65536),
    ((1 << 256) - 1, (1 << 256) - 2),
    ((1 << 256) - 2, (1 << 256) - 1),
    ((1 << 256) - 1, 0),
    (0, (1 << 256) - 1),
)


def generate_nasty_tests(tests, opcodes):
    for opcode in opcodes:
        for a, b in NASTY_AB_VALUES:
            tests.append((opcode, a, b))
